import pygame
from scrollbar import ScrollBar
import os
import button
from grid import Grid

os.environ["SDL_VIDEO_WINDOW_POS"] = "%d,%d" % (100, 25)
pygame.init()

SCREEN_WIDTH = 1200
SCREEN_HEIGHT = 1200
SCROLL_HEIGHT = 1400

screen = pygame.display.set_mode((SCREEN_WIDTH, SCREEN_HEIGHT))
pygame.display.set_caption("Sudoku Bombs")

game_font = pygame.font.SysFont("Arial", 50) 
game_font2 = pygame.font.SysFont("Arial", 20)

grid = Grid(pygame, game_font)

start_img = pygame.image.load("START_BUTTON.png").convert_alpha()
quit_img = pygame.image.load("QUIT_BUTTON.png").convert_alpha()
tuto_img = pygame.image.load("TUT_BUTTON.png").convert_alpha()
back_img = pygame.image.load("BACK_BUTTON.png").convert_alpha()

start_button = button.Button(450, 150, start_img, 1.5)
quit_button = button.Button(450, 300, quit_img, 1.5)
tuto_button = button.Button(450, 450, tuto_img, 1.5)
back_button = button.Button(450, 650, back_img, 1.5)

state = "menu"
run = True

scrollbar = ScrollBar(1150, 0, SCREEN_HEIGHT)
scroll = pygame.Surface((SCREEN_WIDTH, SCROLL_HEIGHT))
scroll_offset = 0

while run:
    for event in pygame.event.get():
        scrollbar.handle_event(event)

        if event.type == pygame.QUIT:
            run = False

        if state == "game":
            if event.type == pygame.KEYDOWN:
                if event.key == pygame.K_TAB:
                    grid.active_bomb_input = (grid.active_bomb_input + 1) % 2
                elif event.unicode.isdigit():
                    i = grid.active_bomb_input
                    grid.bomb_answers[i] = event.unicode
                elif event.key == pygame.K_BACKSPACE:
                    i = grid.active_bomb_input
                    grid.bomb_answers[i] = ""
                elif event.key == pygame.K_RETURN:
                    grid.submit_bomb_answer()
                elif event.key == pygame.K_SPACE and getattr(grid, "restart_allowed", False):
                    grid.restart()
                    grid.restart_allowed = False

            if event.type == pygame.MOUSEBUTTONDOWN and event.button == 1:
                mouse_x, mouse_y = pygame.mouse.get_pos()
                adjusted_mouse_y = mouse_y + scroll_offset
                grid.get_mouse_click(mouse_x, adjusted_mouse_y)

    scroll_offset = int(scrollbar.scroll_percent * (SCROLL_HEIGHT - SCREEN_HEIGHT))

    if state == "menu":
        screen.fill((202, 228, 241))
        if start_button.draw(screen):
            state = "game"
        if tuto_button.draw(screen):
            state = "tutorial"
        if quit_button.draw(screen):
            run = False

    elif state == "game":
        scroll.fill((0, 0, 0))
        grid.draw_all(pygame, scroll)

        sudoku_complete = grid.check_grids()
        bombs_correct = all(grid.bomb_cell_correct.get(b, False) for b in grid.bombs)
        bombs_wrong = any(bomb in grid.bomb_cell_correct and not grid.bomb_cell_correct[bomb] for bomb in grid.bombs)

        grid.restart_allowed = False

        if sudoku_complete and bombs_correct:
            grid.win = True
            grid.restart_allowed = True
            won_surface = game_font2.render("Tu uzvarēji!", False, (0, 255, 0))
            press_space_surf = game_font2.render("Spied space, lai restartētu.", False, (0, 255, 200))
            scroll.blit(won_surface, (956, 650))
            scroll.blit(press_space_surf, (920, 670))

        elif bombs_wrong:
            grid.win = False
            grid.restart_allowed = True
            fail_surface = game_font2.render("Kļūda bombā! Restartē.", False, (255, 0, 0))
            press_space_surf = game_font2.render("Spied space, lai restartētu.", False, (255, 80, 80))
            scroll.blit(fail_surface, (920, 650))
            scroll.blit(press_space_surf, (920, 670))

        screen.blit(scroll, (0, -scroll_offset))
        scrollbar.draw(screen)

    elif state == "tutorial":
        scroll.fill((255, 255, 255))
        y = 20
        font = pygame.font.SysFont("Arial", 25)
        text = [
            "NOTEIKUMI:",
            "* Spēkā ir parastie sudoku noteikumi.",
            "* Spēlē ir fiksēts bombu skaits (2 bombas).",
            "* Sudoku laukā ir redzamas bombu atrašanās vietas.",
            "* Katrai bombai ir norādīts, vai tā ir B1 vai B2.",
            "",
            "* Zem sudoku lauka ir ievades vieta bombas ciparam.",
            "* Jāievada skaitlis no 1 līdz 9, kas atbilst bombas vietai sudoku 3x3 apakšrežģī.",
            "",
            "* Lai pārietu uz nākamo bombu (B1 vai B2), jānospiež TAB.",
            "* Lai pārbaudītu ievadītos rezultātus, jānospiež ENTER.",
            "",
            "ZAUDĒJUMA NOSACĪJUMI:",
            "* Spēle tiek zaudēta, ja tiek nospiests ENTER,",
            "* bet viena vai abas bombu vērtības ir ievadītas nepareizi.",
            "",
            "UZVARAS NOSACĪJUMI:",
            "* Spēle tiek uzvarēta, ja visi sudoku lauciņi ir aizpildīti pareizi",
            "* un ir ievadīti pareizie cipari zem abām bombām."
        ]

        for line in text:
            surface = font.render(line, True, (0, 0, 0))
            scroll.blit(surface, (20, y))
            y += 35

        mouse_pos = pygame.mouse.get_pos()
        adjusted_mouse_pos = (mouse_pos[0], mouse_pos[1] + scroll_offset)
        if back_button.draw(scroll, adjusted_mouse_pos):
            state = "menu"

        screen.blit(scroll, (0, -scroll_offset))
        scrollbar.draw(screen)

    pygame.display.flip()

pygame.quit()
